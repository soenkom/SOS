void draw_char(UINTN x, UINTN y, UINT32 color, CHAR16 c)
{
    UINT16 *byte = terminus + (UINT64)c * 32;
    UINT32 *base = (UINT32*)GOP->Mode->FrameBufferBase + GOP->Mode->Info->PixelsPerScanLine * x + y;
    UINTN i, j;
    for (i = 0; i < 32; i++) {
        for (j = 0; j < 16; j++) {
            if ((*byte) & (1 << (15 - j))) {
                *(base + GOP->Mode->Info->PixelsPerScanLine * i + j) = color;
            }
        }
        byte++;
    }
}